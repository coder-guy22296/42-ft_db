#include "ft_db.h"

void	display_table(t_table *table)
{
	t_row	*cur;

	cur = table->row;
	printf("===========================\n");
	while (cur)
	{
		printf("%s=%s\n", cur->key, cur->value);
		cur = cur->next;
	}
	printf("===========================\n");
}
