#include "ft_db.h"

void	ft_stradel(char ***arr)
{
	int i;

	i = 0;
	while ((*arr)[i] != '\0')
	{
		free((*arr)[i]);
		i++;
	}
	free(*arr);
	*arr = NULL;
}
